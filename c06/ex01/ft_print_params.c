#include <unistd.h>

void	ft_putstr(char *str)
{
	char	*c;

	c = str;
	while (*c)
	{
		write(1, c, 1);
		c++;
	}
	write(1, "\n", 1);
}

int	main(int argc, char **argv)
{
	int	i;

	i = 1;
	while (i < argc)
	{
		ft_putstr(argv[i]);
		i++;
	}
	return (0);
}
