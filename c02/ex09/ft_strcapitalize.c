char	*ft_strcapitalize(char *str)
{
	int		i;
	char	*c;

	i = 0;
	c = str;
	while (*c != '\0')
	{
		if (*c >= 'A' && *c <= 'Z')
			*c += 32;
		if (*c >= 'a' && *c <= 'z')
		{
			if (i == 0)
				*c -= 32;
			else if ((*(c - 1) < '0') || ((*(c - 1) > '9') && (*(c - 1) < 'A'))
				|| ((*(c - 1) < 'a' && *(c - 1) > 'Z') || (*(c - 1) > 'z')))
				*c -= 32;
		}
		c++;
		i++;
	}
	return (str);
}
