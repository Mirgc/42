int	ft_str_is_alpha(char *str)
{
	char	c;

	c = 0;
	while (*str != '\0')
	{
		c = *str;
		if ((c < 'A') || ((c < 'a' && c > 'Z') || (c > 'z')))
			return (0);
		str++;
	}
	return (1);
}
