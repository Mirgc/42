int	ft_is_prime(int nb)
{
	int	i;
	int	aux;

	i = 2;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (0);
	else
	{
		while (i < nb)
		{
			aux = nb % i;
			if (aux == 0)
				return (0);
			i++;
		}
		return (1);
	}
}
