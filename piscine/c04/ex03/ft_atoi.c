int	ft_atoi(char *str)
{
	char	*c;
	int		mult;
	int		i;

	mult = 1;
	i = 0;
	c = str;
	while (*c == ' ' || *c == '\t' || *c == '\r'
		|| *c == '\f' || *c == '\v' || *c == '\n')
		c++;
	while (*c == '-' || *c == '+')
	{
		if (*c == '-')
			mult *= -1;
		c++;
	}
	while (*c >= '0' && *c <= '9')
	{
		i *= 10;
		i += (*c - '0');
		c++;
	}	
	return (i * mult);
}
